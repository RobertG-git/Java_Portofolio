package C18_ArraysInJava;

public class ClassEx2 {

    // Utilizarea campului 'length'

    public static void main(String[] args) {

        int[] intVals = new int[100];
        double[] doubleVals = new double[52];

        System.out.println("The length of 'intVals' is: " + intVals.length);
        System.out.println("The length of 'doubleVals' is: " + doubleVals.length);

    }


}

package C4_3_OperatoriAritmetici_Inlantuire_Precedenta;

public class ClassEx3 {

	public static void main(String[] args) {
		
	
	//nr. intregi:
	int intOp1 = 25;
	int intOp2 = 15;
	
	int result;
	
	result = intOp1 + intOp2;
	System.out.println(intOp1 + "+" + intOp2 + "=" + result );

	result = intOp1 % intOp2;
	System.out.println(intOp1 + "%" + intOp2 + "=" + result );

	//nr. reale
	
	double intOp11 = 25;
	double intOp21 = 15;
	
	double result1;
	
	result1 = intOp11 + intOp21;
	System.out.println(intOp11 + "+" + intOp21 + "=" + result1 );

	result1 = intOp11 % intOp21;
	System.out.println(intOp11 + "%" + intOp21 + "=" + result1 );
	
	
	}

}

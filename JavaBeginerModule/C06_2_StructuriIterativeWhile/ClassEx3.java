package C6_2_StructuriIterativeWhile;

public class ClassEx3 {

	public static void main(String[] args) {
		 
		int finalCountdown = 100;
		
		while (finalCountdown > 0) {
			System.out.println("Au mai ramas " + finalCountdown + " secunde din 2020!");
			finalCountdown--;
		}
		
		System.out.println("La multi ani 2021!");
	}

}

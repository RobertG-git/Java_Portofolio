package C5_1_OperatoriInJava_continuare;

public class Ex4 {

	public static void main(String[] args) {
		
		int pleaseNegateMe = 100;
		
		pleaseNegateMe = 100 - 200;
		System.out.println(pleaseNegateMe);
		
		pleaseNegateMe = 100;
		pleaseNegateMe *= -1;
		System.out.println(pleaseNegateMe);
		
		pleaseNegateMe = 100;
		pleaseNegateMe /= -1;
		System.out.println(pleaseNegateMe);
		
		pleaseNegateMe = 100;
		pleaseNegateMe -= 200;
		System.out.println(pleaseNegateMe);

	}

}

package C21_PrelucrareaSirurilorStringBuilder.Ex1;

public class ConvertStrings {

    public static int parseInt(String myString){
        return Integer.parseInt(myString);
    }

    public static double parseDouble(String myString){
        return Double.parseDouble(myString);
    }

    public static boolean parseBoolean(String myString){
        return Boolean.parseBoolean(myString);
    }

}
